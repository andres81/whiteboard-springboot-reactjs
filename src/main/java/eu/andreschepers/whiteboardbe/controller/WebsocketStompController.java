package eu.andreschepers.whiteboardbe.controller;

import lombok.RequiredArgsConstructor;
import org.springframework.messaging.handler.annotation.DestinationVariable;
import org.springframework.messaging.handler.annotation.MessageMapping;
import org.springframework.messaging.simp.SimpMessagingTemplate;
import org.springframework.stereotype.Controller;

@Controller
@RequiredArgsConstructor
public class WebsocketStompController {

    private final SimpMessagingTemplate simpMessagingTemplate;

    @MessageMapping("/whiteboard/{sessionId}")
    public void handle(String message, @DestinationVariable String sessionId) {
        simpMessagingTemplate.convertAndSend("/topic/"+sessionId, message);
    }
}